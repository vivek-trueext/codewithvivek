let mainCorseImg = document.getElementById('maincourseimg');
let menulist1 = document.getElementById('menulist1');
let leftimgbox1 = document.getElementById('leftimgbox1');
let mainCourse = document.getElementById('maincourse');

mainCorseImg.addEventListener('click', function () {
    menulist1.style.transform = 'scale(1)';
    leftimgbox1.style.width = "38vw";
    leftimgbox1.style.transitionDuration = "1s";
    mainCourse.style.transform = "rotate(-90deg)";

});

